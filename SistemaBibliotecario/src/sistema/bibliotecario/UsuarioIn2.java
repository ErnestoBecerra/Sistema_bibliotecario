    @Override
    public void actionPerformed(ActionEvent e) {
        if(e.getSource()== jButton1){
           UsuarioIn Ventana= new UsuarioIn();
           Ventana.setVisible(true);
           dispose();
        }
        if(e.getSource()== jButton2){
            String Nu=Telefono.getText();
            String Dir=Domicilio.getText();
            String curp=Curp.getText();
            String nombre = Nombre.getText();
            String apellido = Apellido.getText();
            String ID = "";
            if( !jCheckBox1.isSelected() || Nu.equals("") || Dir.equals("") || curp.equals("") || nombre.equals("") || apellido.equals("")){
                Resultado.setText("Hay campo(s) sin llenar");
            }
            else {
                ID = String.valueOf(curp.charAt(0)) + String.valueOf(curp.charAt(1));
                for(int i = 0; i < 2; i++) {
                    ID += String.valueOf((int)(Math.random() * 9 + 1));
                }
                Registro usuario = new Registro(Nu, curp, true, ID, nombre, apellido, Dir);
                Resultado.setText("ID: " + usuario.getID());
            }
        }
    }
